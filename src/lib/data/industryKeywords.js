/**
 * 獲取行業和職位相關的關鍵詞
 * @param {String} industry - 行業類型
 * @param {String} position - 職位類型 (可選)
 * @returns {Array} - 關鍵詞數組
 */
export function getIndustryKeywords(industry, position = "") {
  // 基本行業關鍵詞
  const baseKeywords = getBaseIndustryKeywords(industry);

  // 職位特定關鍵詞
  const positionKeywords = getPositionKeywords(industry, position);

  // 合併並去重
  return [...new Set([...baseKeywords, ...positionKeywords])];
}

